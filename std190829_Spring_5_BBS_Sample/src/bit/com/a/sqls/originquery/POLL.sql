CREATE TABLE POLL(
    POLLID NUMBER(10) NOT NULL,
    ID VARCHAR2(400) NOT NULL,
    QUESTION VARCHAR2(4000) NOT NULL,
    SDATE DATE ,
    EDATE DATE ,
    ITEMCOUNT NUMBER(10) ,
    POLLTOTAL NUMBER(10) ,
    REGDATE DATE ,
    CONSTRAINT POLL_PK PRIMARY KEY(POLLID)
);

